export type AppLanguage = keyof typeof translations;
export type TranslationKey = keyof typeof translations.en;
export type AppText = Record<TranslationKey, string>;
export type LastSavedKey =
  | "autosaved"
  | "autosavedJustNow"
  | "unsavedChanges"
  | "openedLocalDraft"
  | "openedFromDisk"
  | "savedToDisk"
  | "wechatHtmlCopied"
  | "htmlExported";

export const defaultLanguage: AppLanguage = "en";
export const languageOptions: { id: AppLanguage; labelKey: TranslationKey }[] = [
  { id: "en", labelKey: "english" },
  { id: "zh", labelKey: "chinese" }
];

export function isLanguage(language: string | null): language is AppLanguage {
  return language === "en" || language === "zh";
}

export function getLanguage() {
  const language = localStorage.getItem("tickpad:language");
  return isLanguage(language) ? language : defaultLanguage;
}
